package com.crescendo.application.model;

import java.util.Date;

public class Review {

    private String userName;
    private String address;
    private String friendCount;
    private String reviewCount;
    private String photoCount;
    private String rating;
    private String reviewDate;
    private String comment;
    private String anger;
    private String joy;
    private String suprise;
    private String sorrow;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getFriendCount() {
        return friendCount;
    }

    public void setFriendCount(String friendCount) {
        this.friendCount = friendCount;
    }

    public String getReviewCount() {
        return reviewCount;
    }

    public void setReviewCount(String reviewCount) {
        this.reviewCount = reviewCount;
    }

    public String getPhotoCount() {
        return photoCount;
    }

    public void setPhotoCount(String photoCount) {
        this.photoCount = photoCount;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public String getReviewDate() {
        return reviewDate;
    }

    public void setReviewDate(String reviewDate) {
        this.reviewDate = reviewDate;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public String getAnger() {
        return anger;
    }

    public void setAnger(String anger) {
        this.anger = anger;
    }

    public String getJoy() {
        return joy;
    }

    public void setJoy(String joy) {
        this.joy = joy;
    }

    public String getSuprise() {
        return suprise;
    }

    public void setSuprise(String suprise) {
        this.suprise = suprise;
    }

    public String getSorrow() {
        return sorrow;
    }

    public void setSorrow(String sorrow) {
        this.sorrow = sorrow;
    }
}
